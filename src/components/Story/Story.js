const {candidates} = getData();

export default class Story extends Component {
    render(){
      const styles = require('./Story.scss');
      const {id} = this.props;
      let title, author, date, content;
      if(StoryData[id]){
        title = <h2>{StoryData[id].title}</h2>;
        author = <div className={styles.author}>{StoryData[id].author}</div>;
        date = <div className={styles.date}>{StoryData[id].date}</div>;
        content = StoryData[id].content.map((item, i)=>{
            if(item.slice(0,5) === "http:"){
              return (
                  <img src={item} />
              )
            }else if(item.slice(0,1)==="【"){
              return (
                  <h3>{item}</h3>
              )
            }else if(item.slice(0,2)==="::"){
              return (
                  <p className={styles.subtitle}>{item.slice(2)}</p>
              )
            }else{
              return (
                  <p>{item}</p>
              )
            }
        });
      }else{

        if(candidates[id]){
            content = <p className={styles.noStory}>目前沒有這位候選人的人物誌。</p>;
        }else{
            content = <p className={styles.noStory}>我們僅製作<b>區域立委參選人</b>之人物誌。</p>;
        }
        
      }
      return (
        <article className={styles.Story}>
            {title}
            {author}
            {date}
            {content}
        </article>
      )
    }
}
